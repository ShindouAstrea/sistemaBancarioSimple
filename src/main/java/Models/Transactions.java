package Models;

/**
 *
 * @author ShindouAstrea
 */
public class Transactions {
    private String type ;
    private int value ;

    public Transactions(String type, int value) {
        this.type = type;
        this.value = value;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }
    
}
